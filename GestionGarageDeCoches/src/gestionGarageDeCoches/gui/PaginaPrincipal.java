    private Propietario recogerDatos(){
        Vehiculo vehiculo=new Vehiculo();
        String nombre=jTextFieldNombre.getText();
        String apellidos=jTextFieldApellidos.getText();
        String telefono=jTextFieldTelefono.getText();
        Date fecha=(Date) jSpinnerFechaEntrada.getValue();
        String dni=jTextFieldDNI.getText();
        String tipo= (String) jComboBoxTipoVehiculo.getSelectedItem();
        if(tipo.equalsIgnoreCase("Camión")){
          vehiculo=camion.crearCamion(tipo);
        }else{
            vehiculo.setTipo(tipo);
        }
        String modelo=jTextFieldModelo.getText();
        String matricula=jTextFieldMatricula.getText();
        Propietario p=new Propietario(nombre, apellidos, telefono, fecha, dni, vehiculo, modelo, matricula);
        return p;
    }
