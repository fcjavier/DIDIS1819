    private void jButtonAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaActionPerformed
        boolean acept=lg.comprobarDNI(jTextFieldDNI.getText().trim());
        boolean tlfCorrecto=lg.comprobarTelefono(jTextFieldTelefono.getText().trim());
        if(acept==true && tlfCorrecto==true){
        Propietario p=recogerDatos();
        lista.anhadirAlta(p);
        limpiar();
        }
        if(acept==false){
            ErrorDni error=new ErrorDni(this, true);
            error.setVisible(true);
            jTextFieldDNI.setText("");
        }
        if(tlfCorrecto==false){
            ErrorTelefono errorTelefono=new ErrorTelefono(this, true);
            errorTelefono.setVisible(true);
            jTextFieldTelefono.setText("");
        }
        
    }//GEN-LAST:event_jButtonAltaActionPerformed

